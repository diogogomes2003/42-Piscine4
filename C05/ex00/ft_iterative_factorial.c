#include <stdio.h>

int	ft_atoi(char *str)
{
	int	j;
	int	i;
	int	a;

	j = 1;
	i = 0;
	a = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			j *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		a = (a * 10) + (str[i] - '0');
		i++;
	}
	a *= j;
	return (a);
}

int	ft_iterative_factorial(int nb)
{
	unsigned int	r;

	r = 1;
	while (nb > 0)
	{
		r *= nb;
		nb--;
	}
	if (nb < 0)
	{
		return (0);
	}
	return (r);
}
int	main()
{
	for (int i = 1; i < 11; i++)
	{
		printf("%i! = %i \n", i, ft_iterative_factorial(i));
	}	
	return (0);
}
